let a = 3, b = 2;

let z = a + b;
console.log("Suma: " + z);

z = a - b;
console.log("Resta: " + z);

z = a * b;
console.log("Multiplicacion: " + z);

z = a / b;
console.log("Multiplicacion:" + z);

z = a % b;
console.log("Residuo de division: " + z);

z = a ** b;
console.log("Operacion de exponete: " + z);

//Incremento
//Pre-incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//Post-incremento(el operador ++ despues de la variable)

z = b++;
console.log(b);
console.log(z);

//Pre-Decremento
z = --a;
console.log(a);
console.log(a);

//Post-decremento
z = b--;
console.log(b);
console.log(z);
